<h1>Welcome to CoteWad!</h1>

<p>CoteWad is a web-based application that allows users to edit documents simultaneously. Users are created by a register form with a unique username and e-mail. Then the user logs in with the details he entered. CoteWad also provides a chat between loged user to exchange opinions, ideas and feelings about the app or to share links to their documents in order to start the teamwork. </p>

<p>The aplication uses the Google API for storing and editing the document. It uses AJAX technology to verify login and register details and to display chat messages</p>

<p>We hope you enjoy your visit!</p>